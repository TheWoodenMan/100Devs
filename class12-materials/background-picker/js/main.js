let bodyID = document.querySelector("body").style;
document.getElementById("purple").onclick = runPurple;
document.getElementById("green").onclick = runGreen;
document.getElementById("blue").onclick = runBlue;

function runPurple() {
  bodyID.backgroundColor = "purple";
  bodyID.color = "white";
}

function runGreen() {
  bodyID.backgroundColor = "green";
  bodyID.color = "white";
}

function runBlue() {
  bodyID.backgroundColor = "blue";
  bodyID.color = "white";
}

